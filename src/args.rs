use clap::{Args, Parser, Subcommand};

#[derive(Parser)]
#[command(author, version, about, long_about = None)]
#[command(propagate_version = true)]
pub struct Cli {
    #[command(subcommand)]
    pub command: Command,
}

#[derive(Subcommand)]
pub enum Command {
    /// Login to strava.
    Login,

    /// Download
    Download(DownloadArgs),
}

#[derive(Args)]
pub struct DownloadArgs {}
